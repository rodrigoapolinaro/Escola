    public void mostrar_dados() {

        try {

            tf_codigo.setText(conecta_aluno.resultSet.getString("codigo"));
            tf_nome.setText(conecta_aluno.resultSet.getString("nome"));
            tf_cidade.setText(conecta_aluno.resultSet.getString("cidade"));
            tf_estado.setText(conecta_aluno.resultSet.getString("estado"));
            tf_telefone.setText(conecta_aluno.resultSet.getString("telefone"));
            tf_nota1.setText(conecta_aluno.resultSet.getString("nota1"));
            tf_nota2.setText(conecta_aluno.resultSet.getString("nota2"));
            tf_nota3.setText(conecta_aluno.resultSet.getString("nota3"));
            tf_nota4.setText(conecta_aluno.resultSet.getString("nota4"));
            float media = Float.parseFloat(conecta_aluno.resultSet.getString
                    ("media"));
            tf_media.setText("" + media);
        }
            catch (SQLException erro) {
                if (navega == 1)
                    JOptionPane.showMessageDialog(null, 
                            "Não existem Registro Anteriores");
                else if (navega == 2) 
                    JOptionPane.showMessageDialog(null,
                            "Esse é o Último Registro");
                else
                    JOptionPane.showMessageDialog(null, 
                            "Não localizou os dados" + erro);
                navega = 0;
            }
    }

