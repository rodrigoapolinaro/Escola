    private void bt_excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_excluirActionPerformed
        
        try {
            String sql = "Select * from alunos where codigo = "
                    + "" + tf_codigo.getText();
            
            conecta_aluno.executeSQL(sql);
            conecta_aluno.resultSet.first();
            
            String nome = "Excluir o aluno: "+conecta_aluno.resultSet.getString(
                    "nome")+(" ?");
            
            int opcao_escolhida = JOptionPane.showConfirmDialog(null, nome,
                    "Excluir"
                    + " aluno", JOptionPane.YES_NO_OPTION);
            
            if (opcao_escolhida == JOptionPane.YES_OPTION) {
                
                sql = "DELETE FROM alunos where codigo ="+tf_codigo.getText();
                int conseguiu_excluir = conecta_aluno.statement.executeUpdate(sql);
                if (conseguiu_excluir == 1) {
                    JOptionPane.showMessageDialog(null, 
                            "Aluno excluido com sucesso");
                    conecta_aluno.executeSQL("select * from alunos");
                    conecta_aluno.resultSet.first();
                    mostrar_dados();
                }
            } 
        } 
            
        catch (SQLException erro) {
            JOptionPane.showMessageDialog(null,
                    "Não foi possível excluir registro");
        }
        
    }//GEN-LAST:event_bt_excluirActionPerformed

